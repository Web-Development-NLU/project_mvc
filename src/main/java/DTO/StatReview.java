package DTO;

public class StatReview {
    private double avg;
    private int sum;

    public StatReview() {
    }

    public double getAvg() {
        return avg;
    }

    public int getSum() {
        return sum;
    }

    public void setAvg(double avg) {
        this.avg = avg;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }
}
